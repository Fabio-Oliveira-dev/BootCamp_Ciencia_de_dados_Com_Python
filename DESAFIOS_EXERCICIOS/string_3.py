nome = 'Fabio Oliveira Da Silva'

print(nome[0]) # Pega o primeiro caracter
print(nome[-1]) # 
print(nome[:9]) # Concidera desde a posição 0 ate o caracter 8
print(nome[10:]) # Apartir de 10 ate o final da string
print(nome[10:16]) # Pega pedaços (substring) da string
print(nome[10:16:2]) # Pega pedaços da string e o 2 elimina de 2 em 2 da string
print(nome[:]) # Se não passei o star e nem o stop ele retorna uma copia da string
print(nome[::-1]) # Espelhar a string usando o step -1 e ele vai criar uma copia da string porém invertida
